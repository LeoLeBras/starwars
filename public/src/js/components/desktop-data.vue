            let informations, waterPer;

            planets.map(planet => {
                if(planet.name.toLowerCase() == param) {
                    informations = planet;

                    if(planet.water >= 50) {
                        waterPer = 'big';
                    }
                    else if (planet.water < 50 && planet.water >= 5) {
                        waterPer = 'small';
                    }
                    else {
                        waterPer = 'xsmall';
                    }
                }
            });

            return {
                ...informations,
                waterPer
